void OLED_Init(void)
{
	//缓存清零
	RAM_Delet();
	//对比度设置，亮度最大 0~127。
	IIC_OLED2864_Write_Command(0x81,0x78);
	IIC_OLED2864_Write_Command(0xff,0x78);
  //上电显示RAM的图像
  //0xA4:RAM的图像
  //0XA5:全亮测试
	IIC_OLED2864_Write_Command(0xA4,0x78);
	//设置正常显示或反相显示
	//A6h, X0 =0：上电默认是黑底白字。
	//A7h, X0 =1：白底黑字。
	IIC_OLED2864_Write_Command(0xA6,0x78);
	//AEh, X[0]=0b:默认睡眠模式，不亮。
	//AFh X[0]=1b:显示开，显示RAM图案。
	IIC_OLED2864_Write_Command(0xAF,0x78);
	//设置映射开始行用来卷屏等操作把 RAM 的第0行映射到液晶玻璃的 COM0 端口
	IIC_OLED2864_Write_Command(0x40,0x78);
	//左右镜面设置 
	//0XA0:列地址 0 映射到 seg0（复位）
	//0XA1:列地址 127 映射到 seg0
	IIC_OLED2864_Write_Command(0xA1,0x78);
	//上下镜面设置
	//0XC0: COM0 扫描到 COM63 （复位）
	//0XC8: COM63 扫描到 COM0
	IIC_OLED2864_Write_Command(0xc8,0x78);
	IIC_OLED2864_Write_Command(0xa8,0x78);
	IIC_OLED2864_Write_Command(0x3f,0x78);
	//设置显示偏移 总共是 0~63 行，假如值是多少，就把 RAM 的第一行映射到液晶玻璃的 COM0 端口第几行。
	IIC_OLED2864_Write_Command(0xD3,0x78);
	IIC_OLED2864_Write_Command(0x00,0x78);
	IIC_OLED2864_Write_Command(0xD5,0x78);
	IIC_OLED2864_Write_Command(0x80,0x78);
	IIC_OLED2864_Write_Command(0xD9,0x78);
	IIC_OLED2864_Write_Command(0x22,0x78);
//IIC_OLED2864_Write_Command(0xDB,0x78);
//IIC_OLED2864_Write_Command(0x20,0x78);
	IIC_OLED2864_Write_Command(0x8D,0x78);
	IIC_OLED2864_Write_Command(0x14,0x78);
	//设置成页寻址模式
	IIC_OLED2864_Write_Command(0x20,0x78);
	IIC_OLED2864_Write_Command(0x02,0x78);
	//画面清除
	OLED_Delet();
}




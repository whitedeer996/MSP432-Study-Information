  /**
	* @brief  iic发送一个命令字节 (byte)  MCU----->oled（命令）
	* @param  command :OLED操作命令字节 Writeaddres：OLED地址 0X78
	* @retval 0
	* @sttention 传输完从机地址之后 ,控制字节或数据字节开始通过SDA传输。一个控制字节主要由CO和D/C#
    *           位后面再加上六个0组成的。
    *          a)如果Co为设为逻辑0后面传输的信息就只包含数据字节。
    *          b)D/C决定了下个数据字节是作为命令还是数据 。 如果D/C# 位设为 0下一个数据就是命令 。
    *            如果D/C# 位设为1，下一个数据就是数据，将会存在 GDDRAM  中。GDDRAM列地址指针将会在
    *            每次数据写之后自动加1。 
	*/


static void IIC_OLED2864_Write_Command(uint8_t command,uint8_t Writeaddres)
{
	  //iic Start
	  i2c_Start();
	  //发送oled设备地址
	  i2c_SendByte(Writeaddres);
	  //等待器件应答信号
	  while(i2c_WaitAck());
	  //发送写入的命令(0x00)
	  i2c_SendByte(0x00);
	  //等待器件应答信号
	  while(i2c_WaitAck());
	  //发送一字节要写入的数据
	  i2c_SendByte(command);
	  //等待器件应答信号
	  while(i2c_WaitAck());
	  //iic stop
	  i2c_Stop();

}
/**
  * @brief   MCU向OLED发送一个单元格的数据，一共16个字节
  * @param   PAGE:选择页，0~7页，SEG16：选择列恳灰8个单元，每一个单元16列
  * @retval 0
  * @sttention  发送的数据从三维数组OLED12864_Write_Buffer中获取 
  */ 
void IIC_OLED12864_Write_PhotoData(uint8_t PAGE,uint8_t SEG16)
{
	uint8_t i;
	//写页选命令，0xb0~0xb7,对应的时0~7页
	IIC_OLED2864_Write_Command(0XB0+PAGE,0X78);
	//按照每16列为一个单元。划分成0x10~0x17共八个单元。复位值是0
	IIC_OLED2864_Write_Command(0X10+SEG16,0x78);
	//iic Start
	i2c_Start();
	//发送oled设备地址
	i2c_SendByte(0x78);
	//等待器件应答信号
	while(i2c_WaitAck());
	//发送写入的命令(0x40)
	//0x40表示写入的是图像数据
	i2c_SendByte(0x40);
	//等待器件应答信号
	while(i2c_WaitAck());
	for(i=0;i<16;i++)
	{
		//发送一字节要写入的数据
		i2c_SendByte(OLED12864_Write_Buffer[PAGE][SEG16][i]);
		//等待器件应答信号
		while(i2c_WaitAck());
	}
	//iic stop
	i2c_Stop();
}
/**
  * @brief   将OLED图像全部清除
  * @param    
  * @retval 
  * @sttention 清除数据是向OLED写入0x00  
  *
  */ 
void OLED_Delet(void)
{

	//i:8页
	//j:每页对应的8个单元格
	//k:每个单元格对应的16个字节
	uint8_t i,j,K;
	for(i=0;i<8;i++)
	{
		for(j=0;j<8;j++)
		{
			//写页选命令，0xb0~0xb7,对应的时0~7页
			IIC_OLED2864_Write_Command(0XB0+i,0X78);
			//按照每16列为一个单元。划分成0x10~0x17共八个单元。复位值是0
			IIC_OLED2864_Write_Command(0X10+j,0x78);
			//iic Start
			i2c_Start();
			//发送oled设备地址
			i2c_SendByte(0x78);
			//等待器件应答信号
			while(i2c_WaitAck());
			//发送写入的命令(0x40)
			//0x40表示写入的是图像数据
			i2c_SendByte(0x40);
			//等待器件应答信号
			while(i2c_WaitAck());
			for(K=0;K<16;K++)
			{
				//发送一字节要写入的数据
				i2c_SendByte(0);
				//等待器件应答信号
				while(i2c_WaitAck());
			}
			//iic stop
			i2c_Stop();
			}
	} 
}
/**
  * @brief  缓存清零
  * @param  无
  * @retval 无
  * @sttention 通过指针的方式向清零OLED12864_Write_Buffer[8][8][16]  8*8*16=1024。
  */
void RAM_Delet(void)
{
	uint16_t i;
	uint8_t *p;
	p=&OLED12864_Write_Buffer[0][0][0];
	for(i=0;i<1024;i++)
	{
		*p=0X00;
		p++;
	}	
}

/**
  * @brief  OLED显示BMP图片
  * @param  meg 缓冲区指针
  * @retval 无
  */
void OLED_Display_Photo(uint8_t const *meg)
{
	uint8_t i=0,j=0,k=0;
	uint16_t m=0;
	uint8_t n=0;
	//8个页
	for(i=0;i<8;i++)
	{
		//每页128个字节
		for(j=0;j<8;j++)
		{
			//写页选命令，0xb0~0xb7,对应的时0~7页
			IIC_OLED2864_Write_Command(0XB0+i,0x78);
			//按照每16列为一个单元。划分成0x10~0x17共八个单元。复位值是0
			IIC_OLED2864_Write_Command(0X10+n,0x78);
			//iic Start
			i2c_Start();
			//发送oled设备地址
			i2c_SendByte(0x78);
			//等待器件应答信号
			while(i2c_WaitAck());
			//发送写入的命令(0x40)
			//0x40表示写入的是图像数据
			i2c_SendByte(0x40);
			//等待器件应答信号
			while(i2c_WaitAck());
			for(k=0;k<16;k++)
			{
				//发送一字节要写入的数据
				i2c_SendByte(meg[m++]);
				//等待器件应答信号
				while(i2c_WaitAck());
			}
			n++;
		    n=n%8;
		}
		//iic stop
		i2c_Stop();
	}
}
	
